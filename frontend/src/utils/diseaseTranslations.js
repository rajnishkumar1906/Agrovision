// Helper function to get translated disease name
export const getTranslatedDisease = (diseaseKey, language) => {
  if (!diseaseKey) return 'Unknown';
  
  // If the disease is already in readable format (not the API key format)
  if (!diseaseKey.includes('___')) {
    return diseaseKey;
  }
  
  // Get translation from the dictionary
  const translation = diseaseTranslations[language]?.[diseaseKey];
  if (translation) {
    return translation;
  }
  
  // Fallback: format the disease key nicely
  const parts = diseaseKey.split('___');
  if (parts.length >= 2) {
    const crop = parts[0];
    const disease = parts[1].replace(/_/g, ' ');
    return `${crop}: ${disease}`;
  }
  
  return diseaseKey.replace(/_/g, ' ');
};

